    private void cb_penjualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_penjualActionPerformed
        if (cb_penjual.getSelectedItem().equals("Pilih ID Penjual")){
            t_namapenjual.setText("");
        }else{
            try {
                Connection c = koneksi.GetConnection();
                Statement s = c.createStatement();

                String sql = "SELECT nama FROM penjual WHERE id ='" + cb_penjual.getSelectedItem() + "'";
                ResultSet r = s.executeQuery(sql);

                while (r.next()) {
                    t_namapenjual.setText(r.getString("nama"));
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_cb_penjualActionPerformed

